/*program to illustrate simple if
(find area of circle only if r is less than 5*/
class simpleif1
{
     public static void main(String args[])
       {
         double r=3.4;
        if(r<5)
          {
		     double area=3.142*r*r;
             System.out.println("RSDIOUS="+r+"\nAREA OF CIRCLE="+area );
          }
     }
}